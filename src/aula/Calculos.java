package aula;

public class Calculos {
	double soma(double x, double y) {
		double total = x + y;
		return total;
	}

	double subtracao(double x, double y) {
		double total = x - y;
		return total;
	}

	double multiplicacao(double x, double y) {
		double total = x * y;
		return total;
	}

	double divisao(double x, double y) {
		double total = x / y;
		return total;
	}
}
